package Basics;

public class VariablesAndUpdation {
    public static void main(String[] args) {
        int x = 5;  //declaration and initialization
        System.out.println(x);
        x = 7; //updation
        System.out.println(x);
        x = x + 3; // updation
        System.out.println(x);
        x = x - 15;  //updation
        System.out.println(x);
    }
}
