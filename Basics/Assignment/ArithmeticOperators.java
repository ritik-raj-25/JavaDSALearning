package Basics.Assignment;

public class ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic Operations on Integers
        int x = 5;
        int y = 2;
        System.out.println("Integer Addition Result:"+(x+y));
        System.out.println("Integer Substraction Result:"+(x-y));
        System.out.println("Integer Multiplication Result:"+(x*y));
        System.out.println("Integer Division Result:"+(x/y));
        // Arithmetic Operations on floats
        float a = (float)12.4;
        float b = 15.4f;
        System.out.println("Float Addition Result:"+(a+b));
        System.out.println("Float Substraction Result:"+(a-b));
        System.out.println("Float Multiplication Result:"+(a*b));
        System.out.println("Float Division Result:"+(a/b));
        // Arithmetic Operations on doubles
        double p = 22.5;
        double q = 7.5f;
        System.out.println("Double Addition Result:"+(p+q));
        System.out.println("Double Substraction Result:"+(p-q));
        System.out.println("Double Multiplication Result:"+(p*q));
        System.out.println("Double Division Result:"+(p/q));
    }   
}
